CREATE VIEW [dbo].[II_GL_REPORTE4]
AS

SELECT  str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)) AnioMes ,
    GL20000.JRNENTRY, GL20000.SOURCDOC, GL20000.REFRENCE, GL20000.TRXDATE, GL20000.LSTDTEDT, GL20000.CRDTAMNT, GL20000.DEBITAMT,
    GL00100.ACTDESCR,
    SY00900.SDOCDSCR,
    GL00105.ACTNUMST
FROM
    { oj ((PRD04.dbo.GL30000 GL20000	
				INNER JOIN PRD04.dbo.GL00100 GL00100 ON GL20000.ACTINDX = GL00100.ACTINDX)
				LEFT OUTER JOIN PRD04.dbo.SY00900 SY00900 ON GL20000.SOURCDOC = SY00900.SOURCDOC)
     INNER JOIN PRD04.dbo.GL00105 GL00105 ON
        GL00100.ACTINDX = GL00105.ACTINDX}
WHERE
	-- tengo que filtrar LOS UNION para que no duplique
	((REFRENCE not LIKE '%etiro%'  and  REFRENCE not like '%fondo%') or
	(REFRENCE not LIKE '%vi%'  and  not REFRENCE like '%tico%') or 
	(REFRENCE not LIKE '%Gasto%'  and  REFRENCE not like '%rendir%') or 
	(REFRENCE LIKE '%etiro%'  and  REFRENCE like '%efectivo%'))
UNION ALL
SELECT str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)) AnioMes ,
    999999 as JRNENTRY, 'K-REFECT' as SOURCDOC, 'Retiro de Efectivo' REFRENCE, 
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103) TRXDATE,
	max(GL20000.TRXDATE) TRXDATE, 
	MAX(GL20000.LSTDTEDT) LSTDTEDT, 
	sum(GL20000.CRDTAMNT) CRDTAMNT, sum(GL20000.DEBITAMT) DEBITAMT,
    'Retiro de Efectivo Acumulado' ACTDESCR, 'Retiro de Efectivo Acumulado' SDOCDSCR, GL00105.ACTNUMST
FROM PRD04.dbo.GL30000 (nolock) as GL20000 
	INNER JOIN PRD04.dbo.GL00100 (nolock) on GL20000.ACTINDX = GL00100.ACTINDX
    INNER JOIN PRD04.dbo.GL00105 (nolock) on GL00100.ACTINDX = GL00105.ACTINDX
WHERE
	REFRENCE LIKE '%etiro%'  and  REFRENCE like '%efectivo%'
	GROUP BY 
	str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)),
	GL00105.ACTNUMST
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103)
union all
SELECT str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)) AnioMes ,
    999999 as JRNENTRY, 'K-RFACUM' as SOURCDOC, 'Retiro de fondos' REFRENCE, 
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103) TRXDATE,
	max(GL20000.TRXDATE) TRXDATE, 
	MAX(GL20000.LSTDTEDT) LSTDTEDT, 
	sum(GL20000.CRDTAMNT) CRDTAMNT, sum(GL20000.DEBITAMT) DEBITAMT,
    'Ret.Fond. Acumulados' ACTDESCR, 'Ret.Fond. Acumulados' SDOCDSCR, GL00105.ACTNUMST
FROM PRD04.dbo.GL30000 (nolock) as GL20000 
	INNER JOIN PRD04.dbo.GL00100 (nolock) on GL20000.ACTINDX = GL00100.ACTINDX
    INNER JOIN PRD04.dbo.GL00105 (nolock) on GL00100.ACTINDX = GL00105.ACTINDX
WHERE
	REFRENCE LIKE '%etiro%'  and  REFRENCE like '%fondo%'
	GROUP BY 
	str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)),
	GL00105.ACTNUMST
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103)
union all
-- Viaticos
SELECT str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)) AnioMes ,
    999999 as JRNENTRY, 'K-VIACUM' as SOURCDOC, 'Viaticos' REFRENCE, 
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103) TRXDATE,
	max(GL20000.TRXDATE) TRXDATE, 
	MAX(GL20000.LSTDTEDT) LSTDTEDT, 
	sum(GL20000.CRDTAMNT) CRDTAMNT, sum(GL20000.DEBITAMT) DEBITAMT,
    'Viaticos Acumulados' ACTDESCR, 'Viaticos Acumulados' SDOCDSCR, GL00105.ACTNUMST
FROM PRD04.dbo.GL30000 (nolock) as GL20000 
	INNER JOIN PRD04.dbo.GL00100 (nolock) on GL20000.ACTINDX = GL00100.ACTINDX
    INNER JOIN PRD04.dbo.GL00105 (nolock) on GL00100.ACTINDX = GL00105.ACTINDX
WHERE
	REFRENCE LIKE '%vi%'  and  REFRENCE like '%tico%' -- por si le mete el asento a la a	
	GROUP BY 
	str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)),
	GL00105.ACTNUMST
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103)
-- Gastos a Rendir
union all
SELECT str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)) AnioMes ,
    999999 as JRNENTRY, 'K-GRACUM' as SOURCDOC, 'Gastos a Rendir' REFRENCE, 
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103) TRXDATE,
	max(GL20000.TRXDATE) TRXDATE, 
	MAX(GL20000.LSTDTEDT) LSTDTEDT, 
	sum(GL20000.CRDTAMNT) CRDTAMNT, sum(GL20000.DEBITAMT) DEBITAMT,
    'Gs.a Rendir Acumulados' ACTDESCR, 'Gs.a Rendir Acumulados' SDOCDSCR, GL00105.ACTNUMST
FROM PRD04.dbo.GL30000 (nolock) as GL20000 
	INNER JOIN PRD04.dbo.GL00100 (nolock) on GL20000.ACTINDX = GL00100.ACTINDX
    INNER JOIN PRD04.dbo.GL00105 (nolock) on GL00100.ACTINDX = GL00105.ACTINDX
WHERE
	REFRENCE LIKE '%Gasto%'  and  REFRENCE like '%rendir%'
	GROUP BY 
	str(Year(GL20000.TRXDATE)) + str(month(GL20000.TRXDATE)),
	GL00105.ACTNUMST
	--CONVERT(VARCHAR(25),DATEADD(dd,-(DAY(DATEADD(mm,1,GL20000.TRXDATE))),DATEADD(mm,1,GL20000.TRXDATE)),103)



